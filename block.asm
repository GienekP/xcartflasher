STARTAD	= $4000
READNO	= $37E8
FLASHPR	= $37F0
;-----------------------------------------------------------------------
		OPT h+
;----------------
		ORG STARTAD-8
;----------------
PRC		lda #$FF
		sta READNO
		jmp READNO+1
;----------------
		INI PRC
;-----------------------------------------------------------------------
		ORG STARTAD
;----------------		
		:+16384 dta $FF
;----------------
		INI FLASHPR
